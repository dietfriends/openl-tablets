package org.openl.classloader;

import org.openl.exception.OpenlNotCheckedException;

public class OpenLGeneratedClassAlreadyDefinedException extends OpenlNotCheckedException {
    public OpenLGeneratedClassAlreadyDefinedException() {
    }

    public OpenLGeneratedClassAlreadyDefinedException(String message) {
        super(message);
    }

    public OpenLGeneratedClassAlreadyDefinedException(String message, Throwable cause) {
        super(message, cause);
    }

    public OpenLGeneratedClassAlreadyDefinedException(Throwable cause) {
        super(cause);
    }

}
